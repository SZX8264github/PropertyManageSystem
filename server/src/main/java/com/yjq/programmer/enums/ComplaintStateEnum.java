public enum ComplaintStateEnum {

    WAIT(1,"待受理"),

    FINISH(2,"已受理"),

    ;

    Integer code;

    String desc;

    ComplaintStateEnum(Integer code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
